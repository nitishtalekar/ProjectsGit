                                     <p>
                                    Chocolate, a guilty pleasure of many, this food product is something not just chocoholics but the entire world carves for!<br>
With unique shapes, styles and flavours, chocolate is often used as an expression of gratitude, sympathy, or concern even as a statement of love.<br>
When most of us hear the word chocolate, we picture a bar, a box of bonbons, or a bunny. The verb that comes to mind is probably "eat," not "drink," and the most apt adjective would seem to be "sweet." But for about 90 percent of chocolate's long history, it was strictly a beverage, and sugar didn't have anything to do with it.<br>
As you read this blog ahead, you would come to know some more fascinating tit-bits which will add onto enriching your knowledge about the Chocolate World.</p>
<p>
Let’s take you on a journey on discovering the sweet history, uniqueness & some more different aspects of a chocolate!<br><br>

<b>The Sweet History of a Chocolate</b><br>
A big fact which will blow our minds is that historically chocolate was originally drunk and not eaten. It was actually consumed as a bitter beverage rather than as a sweet edible treat for most of its history. Anthropologists have found evidence that chocolate was produced by pre-Olmec cultures living in present-day Mexico as early as 1900 B.C. <br>
The ancient Mesoamericans who first cultivated cacao plants found in the tropical rainforests of Central America fermented, roasted and ground the cacao beans into a paste that they mixed with water, vanilla, honey, chili peppers and other spices to brew a frothy chocolate drink.<br>
Etymologists trace the origin of the word "chocolate" to the Aztec word "xocoatl" which referred to a bitter drink brewed from cacao beans. The Latin name for the cacao tree, Theobroma cacao, means "food of the gods."<br><br>
Sweetened chocolate didn't appear until Europeans discovered the Americas and sampled the native cuisine. <br>
The creation of the first modern chocolate bar is credited to Joseph Fry, who in 1847 discovered that he could make a mouldable chocolate paste by adding melted cacao butter back into Dutch cocoa. By 1868, a little company called Cadbury was marketing boxes of chocolate candies in England. Milk chocolate hit the market a few years later, pioneered by another name that may ring a bell – Nestle.</p>

<p>
<h3>Cacao Beans</h3><br>
<b>The Making of a Chocolate Bar</b><br>
Chocolate is a product that requires complex procedures to produce. The process involves harvesting cacao, refining cacao to cacao beans, and shipping the cacao beans to the manufacturing factory for cleaning, coaching and grinding.<br>
The process begins with harvesting cacao in a forest and involves Plucking and opening the cacao pods, & then moves to fermenting and drying the cacao seeds.
Ever wondered how the chocolate is manufactured?<br>
There you go, it begins with Roasting and Winnowing the cacao followed by grinding the cacao nibs and ends with Blending Cocoa liquor and moulding the chocolate.
Finally, the chocolate is packaged and distributed around the world.</p>
<p>
<b>The Flavour of Chocolate</b><br><br>
Chocolate taste is still difficult to define. In his book Emperor of Chocolate, Joel Glenn Brenner describes recent research on taste. According to him chocolate is created from a mixture of 1,200 kinds of substances, without a sense that is clearly dominant.<br>
Conching, a new term which you can add to your Chocolate Dictionary, is a critical step not so known to many, plays a crucial role in the flavour development of the chocolate by allowing various volatile flavour components present in the cocoa bean to evaporate.<br>
You must be wondering what are some of the best places to travel for chocolate?<br>

<h3><b>Satisfy your sweet tooth in these chocoholic havens</b></h3><br>
- Maison Cailler, Switzerland - Tour the home of one of Switzerland's oldest chocolate brands in the Swiss village of Broc, where Cailler has operated a plant since 1898. See how the famed chocolate is produced, then treat your senses with a generous sampling in the factory's tasting room.<br>
- Chocolate Hotel, England - To eat, breathe, and sleep chocolate, where better to stay than this chocolate-theme hotel? Chocolate-tasting and chocolate-making classes ensure that chocoholics leave satisfied.<br>
- Valrhona Chocolate, France - Chocoholics will enjoy the chance to sample or buy at the factory shop, while professional chefs can study at Valrhona’s École du Grand Chocolat, a chocolate-cookery school.<br>
- Maya Chocolate, Mexico - Here in the likely birthplace of chocolate—the word itself possibly deriving from the Maya xocoatl—taste hot chocolate Maya style: thick, foamy, bittersweet, and flavoured with chili peppers
</p>

                                       
                                       
                            
