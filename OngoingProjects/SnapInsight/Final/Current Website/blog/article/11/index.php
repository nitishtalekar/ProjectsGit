                                     <p>
                                     Yes, you heard it right!<br>
Jerry Lawson, An African-American self-taught Engineer was the man who pioneered the first cartridge-based gaming system that revolutionised the gaming industry.
With his work on the Fairchild Channel F Console, Jerry was the first to innovate the use of interchangeable cartridges in consumer gaming consoles, changing the landscape of the gaming industry as we know it. Lawson's pioneering design set the standard for the game consoles of today.</p>
<p>
Let’s dive into more about the making of this first cartridge-based gaming system and how it revolutionised gaming- <br>
<b>The story of Jerry Lawson’s making of the gaming system</b><br>
Have you ever “fixed” a video game that didn’t work in your home gaming consul by blowing into the cartridge? If so, not only were you an early adopter of the now 19.72-   billion- video game industry, but you were an early supporter of Gerald “Jerry” Lawson, inventor of interchangeable gaming cartridges.<br>
By the mid-70s, Jerry Lawson made the big move to Silicon Valley to work on military devices for Kaiser Electronics, focusing mostly on aircraft displays. Jerry joined Fairchild Semiconductor, Inc. when he received a better opportunity to work outside the linear structure of the typical office environment at Fairchild.<br>
 Fairchild, located in California, allowed Lawson to join the Homebrew Computer Club, where he met two other prolific tech industry members- Steve Jobs and Steve Wozniak.<br>
Lawson's contributions to videogames began with Demolition Derby, a coin-operated arcade machine that he created in his garage while working at Fairchild in San Francisco.
Fairchild soon heard about Jerry’s extracurricular activities, which put him in some hot water with the company, believing that he was going to try and branch out on his own.
In fact, in the Vintage computing interview, Jerry said </p>
<blockquote class="uza-blockquote d-flex">
                                       
                                        <div class="text">
                                            <h5>"Fairchild found out about it – in fact, it was a big controversy that I had done that. And then, very quietly, they asked me if I wanted to do it for them",</h5>
                                         
                                        </div>
                                    </blockquote>
<p>

Instead of firing the budding gaming engineer, though, Fairchild asked Jerry if he wanted to develop games for them.<br>
That’s how the project to build the first cartridge-based gaming system began!<br>
The project was a top-secret where Jerry would only report to the Vice-President.<br>
Although similar machines were in development at various Arcade gaming companies at the time, the console Lawson's team built for Fairchild was the first cartridge-based gaming system that came to market. Jerry and his team of engineers were embarking into unknown territory. At the time, it was unheard of to bring a multi-game, cartridge-based system to the consumer.<br>
Lawson and his team had to figure out how to plug and unplug the cartridges without the customer being slightly maimed by an explosion in the process.</p><p>
<b>How the cartridge-based gaming system revolutionised gaming</b><br>
Lawson’s development allowed users to change games by removing and inserting different cartridges installed with a specific game’s program instead of having a system that was capable of only playing one game that was programmed into the systems hardware.<br>
The Fairchild Channel F System, as it was officially called, was always meant to be an at-home gaming machine. It was its cartridge technology set, however, that set it above the competition. <br>
This revolution in the gaming industry, propelled Lawson into the spotlight as one of, if not the, first role model for aspiring African American game programmers.
Jerry Lawson and his work on the Fairchild Channel F changed the face of the gaming industry. After the Channel F console, many companies followed Lawson’s ingenuity to create systems with cartridge-based games.<br>
In March 2011, Lawson was honoured as an industry pioneer for his work on the game cartridge concept by the International Game Developers Association (IGDA). 
Thanks to the effort of Jerry Lawson that multiple gaming companies like Nintendo, Sega, Atari, PlayStation, and Xbox have benefited from a format that extended the life of their gaming consoles. And thanks to the variety of games that could be played on cartridges -- and, later, discs -- a multi-million-dollar game development industry was born.
</p>

