                                 <p> 
                            The coming of the COVID-19 pandemic has had a catastrophic impact on the world, with affecting more than 180 countries, the World is really going through challenging and filtering times. Nations across the world have declared Lockdown’s to contain this pandemic by social distancing, with India too taking timely action to contain its spread.</p><br>
<p> The spread of the pandemic over the months has had a cascading effect on multiple industries, ranging from the supply chain of the automobile sector, to the Airline Industry and many more. The impact has also been felt a lot on one industry which has always been in big demand, yes you guessed it right - The Food & Beverages Industry.</p><br><p>
Let’s look into the different facets of how the Novel Corona Virus has had an impact on this industry <br>
<b>Global Impact -</b> <br>
The outbreak of COVID-19 has had a dual impact on the industry with offline food chains like Restaurants and Cafes entirely shutting down, coupled with that we see an upsurge in online food deliveries.<br></p>
<p>The closure of restaurants and Cafes has caused a ripple effect among various related industries such as food production, liquor, wine, fishing and farming.
With the Foot traffic to restaurants and cafes declining by 75% in Latin America, and a decline of 90% in North America and the Middle East, the crisis has been something of a trial by fire for restaurant owners , with a similar situation arising in Europe’s tourist hotspots like Sweden, Italy and Hungary, where the economy’s engines thrive on tourism are on the brink of collapse.</p>
<p>Amidst these distressing times, we have a silver lining in the cloud, where Countries like Canada have shown relative stability in terms of food availability as a result of two long-standing practices – a publicly owned food terminal and a system of supply management.<br>
These two practices have protected the Canadian population from price spikes and the sudden shortage of essentials.</p><br><p>
Let’s now dive into how India has been handling the crisis, and how different institutions across the country are fighting the pandemic with a common spirit of unity.</p>
<p> A country like India where 91 percent of the total workforce coming from the informal sector, which includes agriculture, fishing, daily wage workers that act as the supporting elements of the food industry, The Pandemic hit the Food sector out of the blue.<br>
Even though the timely imposition of the countrywide Lockdown was a wise move to contain the spread, the local food systems were disrupted immensely. The disruption was more lethal, simply due to the fact that the country’s peak harvesting time for crops coincided with it.<br></p>

<p>Some resilient measures taken for protecting the livelihood of many amid the virus </p><br><p>
- An organization which is responsible to feed India in extreme conditions like the one we are facing now, know as Defence Food Research Laboratory, lead from the front and produced tonnes of ready to eat meals with longer shelf life.<br>
- The young start-up engineers from the IT Capital of the country, Karnataka, came up with some tech-driven interventions to make a food delivery application which helped in delivering 1 million meals to the needy in Bangalore.<br>
- The state of Kerala also took up immediate actions to protect food security in the state by innovatively setting up community kitchens with the district administration also playing a big role in it.<br></p>

<p><b>The way forward </b><br>
The Novel Corona Virus possesses an unprecedented challenge for the World. With the Governments across the world taking huge steps to feed their nations and protect their livelihoods, undoubtedly many countries have shown bold and decisive leadership. But it becomes imperative for countries across the world to make food systems more resilient to such situations, so as to manoeuvre the way to making more efficient food systems in the future. </p>
                            

