                                     <p> 
                            Have you ever been so stressed at work that you felt your mind could explode at any moment? Have your ever gone through a burn out at Work? 
As we hustle through the day from making one phone call to the next, and pursue to achieve the tasks on our to-do lists, indeed our work pressures, deadlines and a host of other stressors could take a heavy toll on our physical and mental health.</p><br><p>
In fact, on the lighter side, they say a modern-day corporate worker, is a guy who is stressed, depressed yet well dressed.<br>
And today as we sit inside our homes, with restrictions to move out, the mind makes it even more difficult to focus on our tasks, with many psychologists reporting that Mental Health has been the biggest casualty during the Lockdown, especially amongst children.<br>
According to a survey conducted by the Indian Psychiatry Society, within a week of the start of the lockdown, the number of reported cases of mental illness in India had risen by 20%.<br>
As you read this article, you too might be wondering, that are there any well-tested and effective methods to cope with this mental stress and fatigue so that we can peacefully progress in these times?</p><br>
<p>
Here are a few simple, yet profound ways to manage our emotional and mental well-being in today’s age,  <br>
These tangible tools can immensely help us to flush out the clutter from our mind and rejuvenate our lives. <br>
You can call it the <b>6-P Formula.</b></p>



<p>
 <b>Press Pause </b><br>
As we catch up with the day’s commitments, we must also learn to press the Pause Button and Breathe. Just try this for a moment. Pause, close your eyes and inhale deeply. Then slowly exhale and observe your breath. Pausing to breathe will really help us calm down mentally and cool down our mental energies.<br><br>

<b>People to Inspire</b><br>
As they say ‘Association creates Aspiration’, when we hear from positive people and keep a good company of people who inspire us, we get motivated to aspire higher, which can help us rise above our minds.<br><br>

<b>Prayers</b><br>
Prayer essentially means to offer our gratitude to God for all the gifts we have been blessed with. When we reserve certain time everyday for spiritual practices, they help us build a strong foundation that can give us strength to be peaceful during challenging and difficult times.<br><br>

<b>Penning down your thoughts</b><br>
Awareness is the first critical step to release ourselves from the cage of the mind. A tangible self-help tool like Journal Writing can help us penetrate various layers of mental clutter and brings a state of clarity. <br>

Try this experiment today, resolve to just write for 10 minutes a day. As you write, don’t worry much about what you write, just write whatever comes to your mind and don’t let the pen or the keyboard stop for the next ten minutes.<br>
Start <b>Now</b> and experience the <b>clarity!</b><br><br>

<b>Positive Thinking</b><br>
Just like our Radio sets have different frequencies, and when we tune in to a frequency, we access that frequency.<br>
In a similar way there are various frequencies like Positivity, Compassion, Love, Humility, everywhere and according to what we tune in, we are going to access that.<br>
And How can we access that Positivity which permeates through the Ether?<br><br>

<b>Through Gratitude!</b><br>
Try this Exercise – Reflect on the last 24 Hours and identify three to five people or situations that you are grateful for. <br>
This will immediately change your state of mind from negativity to positivity!<br><br>

<b>Phone Fast</b><br>
Yes, you heard it right! <br>
<b>Fasting from your Online Life for a few hours every day!</b><br>
Just as a Fasting Day for your Body gives rest to the digestive system, Try a 4-Hour or a 6-hour Fast from Social Media and your phone. A lot of mental muddle would get flushed out of the system; and you’d surely feel an overall sense of peace. <br><br>

As you start following these practices, you will slowly build up an attitude of focusing on things which are important, & this attitude would surely percolate in your Professional & Personal Growth.<br>
<b>Stop going through Life, Start growing through Life</b><br>
As we hustle through the days in meeting our commitments, it becomes really important to give time for our personal growth, to stay ready, relevant and accountable.
And the best gift you can give yourself to grow bigger, is a Great Book!<br>
Yes!<br>
Developing a Reading Habit is a tangible way of striving for growth which keeps the software of the brain updated to reach bigger heights. Start with just 5 Pages a day and experience the nourishment of your intellect.<br>
And as we incorporate the habit of regular reading & following the 6-P Formula, you would really get a wholesome experience of mental and emotional well-being.

