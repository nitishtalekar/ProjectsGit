                                <h3><b>Introduction<b></h3><br>

Undertale made by Indie game developer Toby Fox was one of the best selling games on Steam when it was released in late 2015 and is a 2D RPG which follows the protagonist, a child who falls down into an underground secluded region which is separated from earth by a magic barrier. The player meets various monsters who are friends as well as foes throughout a journey where the player’s actions affect the game’s dialogue, characters, story and ending based on specific key decisions. </p><br>

<h3>The Story of Toby Fox</h3><br>

<p>
Fox had little experience with game development; he and his three brothers often used RPG Maker 2000 to make RPGs, though few were completed. Fox also worked on a number of EarthBound ROM hacks while in high school as he had played the Mother series at the age of 4! </p><br>
<p>Undertale's creation originated from a battle system made using Game Maker Studio. He wanted to develop a role-playing game that was different from the conventional design, which he often found bland. He set out to develop a game with characters that have intriguing back stories and development and unforseen originality.<br>

This game was his debut as a developer and has been made almost entirely by Toby Fox himself besides some of the art, which was made by Temmie Chang as the main art assistant for the game. The funding for the development was through a kickstarter that Toby had made in June of 2013 with a goal of 5000$ USD stating that Undertale was a traditional RPG where no one has to get hurt. However, the game is anything but traditional and features a unique turn based mini bullet hell like combat system which is challenging but fair. The player for once can also talk to the monsters instead and finish every fight in a peaceful way, i.e, without any killing, unlike ordinary role playing games where the monsters are essentially just entities to slaughter. Undertale gives each monster unique characteristics, speech patterns and different ways of interacting with the player which makes encounters with each enemy type more interesting and challenging without having the need to introduce varying difficulties for the player to choose from.<br>

The demo released during the kickstarter received extremely positive feedback and Toby managed to raise 51,124$ USD and had 2398 backers, a sum which was more than 10x the goal he had set. This motivated Fox further and he worked on the game for almost 2.7 years before releasing the masterpiece in September of 2015, which sold more than 500,000 copies in the first 3 months after it’s release on Steam.The game has sold over 2 million copies at 10$ each and Toby Fox has an estimated net worth of 4 Million $ USD.</p>

<h3>The Different Endings </h3><br>

<p>The game has 3 different runs/endings, depending on the actions of the player which are:-<br>

<b>Pacifist</b> - In this run, the player has to get through the game without killing a single enemy. This is the second most difficult route as the player has to stick to his principles and get through the bosses without killing them as well and this was intentional as Toby Fox thought that the good route is generally easier in games and he wanted to do something different.<br>

<b>Neutral</b> - In this type, the player will go through the game as a normal RPG would, you fight and kill monsters, find loot and items and progress through the levels and finish the game. However, this is the easiest run of the three and is not considered the “True Ending”.<br>

<b>Genocide</b> - The hardest run in the game, it involves the merciless killing of every enemy on each level by walking in each room repeatedly until you stop encountering them and involves the player killing off major characters in the game that in previous runs you’d defeat and not murder.<br>
It features the hardest boss fight in the entire game which is against a character who is not the “Final Boss” you fight at the end of each run but actually a character that you cannot fight while you are playing the other two runs. This run involves the game breaking the fourth wall like hinting at the existence of different timelines.  Finishing a Genocide Run actually has a permanent effect on the game files and the game itself which can only be fixed after a clean re-install of the game.</p>

<h3>More on the development and design</h3><br>
<p>
The player is constantly forced to deal with surprises and details throughout the game which can be missed on the first time and different actions provoke different reactions from NPCs, further encouraging multiple playthroughs.<br>
This made the players spend hours trying to get different endings and gags from their favourite characters and lead them to get attached with the same characters and this was one of the main reasons why everyone loved Undertale, thus ensuring its success. </p>

<p>
The game’s entire soundtrack has been composed by Toby Fox himself and most character themes were composed after the characters were made but a few songs, including Megalovania, the soundtrack for the hardest boss battle was actually a prior composition which was used by Toby in an EarthBound ROM Hack he had made.Toby composed most of the level music before as it’d help him design the level and better set the scene The soundtrack was also one of the major factors after the originality that led to the success of the game.</p>
<br><p>
The game’s humour,design and old-school 16 bit graphics make it the perfect combination of the nostalgia and novelty and thus appeal to those who love the classic look of the art style as well as those who look for a new type of gameplay which aims to break the conventions of RPGs.<br>
The contrast of the graphics and gameplay adds to its’ likeability as most gamers associate the art style with games like Pokemon,Chrono Trigger or Final Fantasy and Undertale brings a completely different combat,character development and story. It’s funny, slightly messed up and sometimes extremely scary but the game still has an appeal to kids as well and is quite famous amongst the younger generation.</p>

<p>Ultimately, Undertale succeeded because it captured the heart of the player and made the player care about the game instead of just being a frustration inducing bullet hell game or another adventure RPG. It carefully differentiates itself by creating the intricate balance of lovable characters, action, tension and satisfaction.</p>


