                                     <p> 
                            It’s been about 27 years since the first-person shooter game first
slipped into floppy disk drives across the world over that there’s an
entire generation of console and PC players out there who’ve never
even heard of Doom, let alone played it. </p><p>
Every single First-Person Shooter that’s taken over the world – or
failed miserably – can doff its cap to Id Software’s seminal piece of
software.<br><br>
This game developed by Id Software for MS-DOS truly revolutionised
the gaming industry.</p><p>
Let’s go into understanding what this game was and what made it
unique!<br><br>

Doom is a first-person shooter game where the player controls an
unnamed space marine—later termed the Doomguy—through a
series of levels set in military bases on the moons of Mars and in Hell
as he rips and tears through onslaught of demons trying to kill him.<br>
The game’s plot was an immersive mix of science fiction and horror.<br>
The player, seeing through the eyes of a mere soldier, is sent to
investigate an incident on the space base on the Martian moon
Phobos and unveiling the horrors of the company that commissioned
him.</p><p>
What makes this game so unique is, in a time where games were still
confined to flat 2D sprites in 16-bits, the makers of this game
brought in an immersive use of 2.5D graphics. The game successfully
tricks the player into thinking it is 3D using techniques like ray casting
and binary space partitioning which were methods way ahead of its
time and extremely difficult to implement. If you’re interested in
knowing more about this we recommend this amazing breakdown by
the YouTube channel The Game Theorists :<br><br>
https://www.youtube.com/watch?v=zb6Eo1D6VW8

</p><p>
Doom set the groundwork for networked multiplayer matches and it
promoted the use of mods (or ‘WADs’ as they were known back
then).</p><p>
With its amazing features of realistic gun physics to intricate level
design, this game acted like a prototype to many such games in the
future with many games later being referred to as ‘Doom Clones’.
The game changed the direction of almost every aspect of personal
computer (PC) games, from graphics and networking technology to
styles of play, notions of authorship, and public scrutiny of game
content.</p>

<p>Please refere the screenshot from the game ‘Doom’ above</p><p>

<b>The Game’s concept and Development</b><br>

In May 1992, id Software released Wolfenstein 3D, later called the
&quot;grandfather of 3D shooters&quot;, specifically first-person shooters,
because it established the fast-paced action and technical prowess
commonly expected in the genre and greatly increased the genre&#39;s
popularity.<br><br>
This set the stage for Doom as the next step of this game genre, the<b>
“first-person shooter.”</b><br><br>
Doom added numerous technical and design improvements to the
Wolfenstein 3D model: a superior graphics engine, fast peer-to-peer
networking for multiplayer gaming, a modular design that let authors
outside id create new levels, and a new mode of competitive play
devised by Romero called “death match.”</p>
<p>
Doom was programmed largely in the ANSI C programming language,
with a few elements done in assembly language.</p>

<p><b>Reception & Prominence of the Game</b><br>
In 1994, PC Gamer UK named Doom the third best computer game of
all time. The editors wrote, &quot;Although it&#39;s only been around for a
couple of months, Doom has already done more to establish the PC&#39;s
arcade clout than any other title in gaming history.&quot;<br>
In 1994 Computer Gaming World named ‘Doom’ The Game of the
Year.<br>
The game has been ported to numerous console gaming platforms
both domestically and abroad where it maintained its popularity,
receiving generally favourable critical reception.</p>

                                    
