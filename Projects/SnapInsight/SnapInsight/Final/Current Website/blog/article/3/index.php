                                 <p> The video game or the interactive entertainment industry continues to boom with the emergence and gradual progression of avenues like Indie games playing a prominent role in its growth.</p><br><p>
Let’s dive into discovering various aspects revolving around Indie Games! </p>
<h3>What are Indie Games?</h3><br>
<p>Independent (Indie) games are essentially games created by individuals or small development teams without much financial support of large game publishers, in contrast to most "AAA games".<br>
With a lot of independence and creative freedom available to them, Indie game developers get a lot of scope for innovation and exploration of different mediums to produce unique experiences in art games.</p>

<h3>Let's dive into the history of Indie games or "shareware games".</h3><br><p>
The genesis of this industry can be traced back to the 1970s and 1980s when there was not much prominence of an established video games industry. In the 1990s, indie games were most commonly distributed as shareware or shared from friend to friend and were therefore known as "shareware games". <br>
The Indie Game industry saw a resurgence in early 2000s, with its growth principally driven by the availability of online distribution over the Internet, allowing game developers to sell directly to players and bypassing limitations of retail distribution.</p>

<h3>Notable Games and Developers</h3><br><p>
1) Minecraft – A best- selling game played by many of us, began life as an indie game prior to its acquisition by Microsoft Studios in 2014. <br>
2) Katana Zero – A 2-D action platform video game developed by Askiisoft which is based in North America.<br>
3) Aquaria – A side scrolling adventure game designed by Derek Yu from the independent game company known as Bit Blot.<br>
4) Oxenfree – A mystery graphic adventure game developed and published by Night School Studio.</p>

<h3>An overview of the Indie Game Development process:</h3><p>
Have you ever wondered what exactly goes in the process of making an indie game? Let’s discover the different stages involved!<br>
1) Idea Stage – It involves the consideration of various possibilities. It is about brainstorming a good idea for the game.<br>
2) Core Game Stage – It involves the selection of engines, tools to use for the game and also the basic programming for the game.<br>
3) Design Stage – Deciding on a storyline, creating characters, developing maps and layouts, and thinking up gameplay models and features are at the core of the design process.<br>
4) Production Stage – The decisions of the design stage are executed in this stage.<br>
5) Testing Stage – This stage is really crucial to improve and fix unexpected bugs and system issues before the game is ready for the launch.</p>

<h3>Challenges facing the Indie Game Industry-</h3><p>
Just like in every other field, this industry too throws a set of challenges to conquer, Let’s look into what these challenges are:<br>
1) Lack of Funding – A TIGA survey in 2018 highlighted that 38 per cent of games companies listed it as the main thing holding their business back.<br>
2) Shortage of Resources - The resources available to independent game developers are entirely different from those available to big-name studios.<br>
3) Visibility – Many Indie game developers are not able to get the potential exposure, due to the increase in number of games, which makes it hard to get noticed.</p>
<p>
A developer entering into an industry like Indie Games should consider creating a long-term strategy for funding and distribution to remain competitive in this industry.<br>
<b>Future Prospects for Indie Game Developers</b>
If you’re thinking about getting into an industry like Indie Games with a lot of opportunities hidden in it, train and develop your technical skills, creativity and project management skills to stand out well in this industry.
Coupled with technical skills, a strong base of ethics and values in your work will give you a killer combination to emerge out on top. </p>
                            

