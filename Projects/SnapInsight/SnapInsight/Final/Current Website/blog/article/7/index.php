                                     <p> 
                            Among the 15 billion batteries which are used worldwide, about 89% percent of those are estimated to be alkaline based batteries. These batteries are prone to create problems at the time of disposal.</p><br>
<p><br>                          
To overcome this problem, a more sustainable alternative was invented by a group of students from the Rensselaer Polytechnic Institute in Troy, New York in 2007.<br>
The researchers created an "ink" of carbon nanotubes suspended in water and an organic surfactant. They then heated the paper in an oven to drive off the water. The nanotubes bonded tightly to the paper fibres, creating a highly conductive sheet of paper that functions even when rolled up.</p><br>
<p>
And there was born – <b>The Paper Battery!</b></p>
<p>
<b>Let’s now dive into understanding what makes this battery unique and sustainable to use </b><br>

Let’s start with understanding a few basics of a Paper Battery: <br>
A paper battery is an electric battery engineered to use a spacer formed largely of cellulose (the major constituent of paper). It incorporates [nanoscopic scale] structures to act as high surface-area electrodes to improve conductivity.<br>

It is an ultra-thin, flexible energy storage device that is used as a battery and also as a good capacitor.</p>
<p>
<b>You must be wondering, what goes into its composition?</b><br>

This device is a combination of carbon nanotubes with cellulose based paper. Paper can be integrated into several different forms of batteries, such as electrochemical batteries, biofuel cells, lithium-ion batteries, supercapacitors, and nanogenerators.<br>
These nanotube films act as the electrodes which are embedded in cellulose based paper, soaked in ionic electrolytic liquid. The electrolyte does not contain any water content. So as there is nothing to freeze or evaporate, it can be used in any environmental conditions.<br>
And the most interesting aspect is, the battery can produce power even if it is folded or cut. That’s the biggest advantage of the battery, that that it can be folded, cut or shaped for the required applications without any loss in its efficiency. If the battery is cut into half, the energy produced by them is halved. Stacking more than one paper batteries multiplies its power output.</p>

<p><b>What’s Powering Paper Batteries Today? </b><br>
Energy crisis is a major problem. And energy experts think that the paper battery can be an altogether path-breaking solution to this big problem. Its biodegrading, light weight, non-toxic and flexible properties have potential adaptability to power the next-generation of electronics, medical devices and hybrid vehicles, allowing for radical new designs and medical technologies. <br>
Being cellulose based product, it is easily recyclable and reusable, even with the existing paper recycling techniques. <br>
This battery can be recharged up to 300 times using almost all electrolytes, including bio-salts such as sweat, urine and blood. <br>
This is how its structure is how shown in the images. <br>
With the developing technologies and reduction in the cost of cathode nanotubes, these batteries have manifold applications!

