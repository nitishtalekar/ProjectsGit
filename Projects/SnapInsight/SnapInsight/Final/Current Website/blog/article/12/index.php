                                     <p>
                                     As the world progresses to the use of advanced technologies across different fields, the application of effective wireless technologies in the field of Defence is also showing prominence.<br>
Within the defence world, with warfare gaining dominance in the cyber space, we see that the defence industry is turning to a new generation of systems for faster, cheaper and more flexible communication, security and more.<br>
Today we talk about how are wireless technologies playing a crucial role in the military domain and how these cutting-edge technologies can help in the functioning of Armed Forces in a world marked by network-centric warfare.</p>

<h3>Let’s begin with understanding some basics of Wireless Communication-</h3><p> 
Wireless communication is essentially the electromagnetic transfer of information between two or more points that are not connected by an electrical conductor. It encompasses various types of fixed, mobile, and portable applications, including two-way radios, cellular telephones, personal digital assistants (PDAs), and wireless networking. <br>
Wireless operations permit services, such as mobile and interplanetary communications, that are impossible or impractical to implement with the use of wires.<br>
Improved wireless networks are revolutionising the combat effectiveness of an infantry soldier and is improving the operational capability in defence scenarios.</p>
<p>
Let’s now move into understanding how these wireless technologies are playing a role in improving operational capabilities of defence forces- <br>
- Rapid headquarters deployment: Wireless connectivity can be rapidly deployed, so users in functional workgroups have wired access to each other within command posts, and wireless connection between command posts.<br>
- Headquarters deployment augmentation: Wireless technology makes it easy to establish an immediate LAN connection between functional areas and the communications command post, server vehicle, or shelter and connect them to critical IT resources.<br>
- Network range extension: The range of the network can be extended using wireless technology in scenarios where functional groups are separated by many kilometres. Here, a directional antenna can transmit signals across a significant distance.</p>
<p>
Network deployment speed can be increased by using wireless technologies which help military staff and commanders to stay connected and provide flexibility to move freely.<br>
The integrated use of wireless communications, GPS receivers and thermal sights allows for the implementation of dispersed yet highly coordinated deployments at night or in adverse conditions.</p>

<h3>The Land Warrior System of the U.S Armed Forces</h3><p>
During the 1990s a variety of defence forces from around the globe began to look at the impact of new technologies on combat effectiveness. For the first time the infantry man was studied as a complete combat system, with a view to enhancing his/her mobility, survivability, strike power and communications. <br>
These programmes culminated in September 2000 with the first operational test of Land Warrior system by the American 82nd Airborne Division. The core objective of Land Warrior revolved around the application of commercial technology and its use in conjunction with existing military hardware to provide the infantry man with enhanced situation awareness.<br>
These Land Warrior systems focused on the incorporation of digital maps, intelligence information and imagery from a weapon-mounted thermal weapon sight (TWS).

                                     </p>

<p>
<h3>WET (Wireless Electricity Transmission) of Aglaya</h3><p>
The wireless electrical transmission technology developed by the defence company based in New Delhi promises to charge devices with power requirements of devices between the range of 3 -12 Volts under any weather conditions.<br>
WET transmits electricity over large distances with lab tests confirming range of 5 Kms without wires to charge devices connected to a receiving station. <br>
This station converts incoming signals to electricity and passes this current to a USB output, enabling of Commercial devices such as Car Batteries, High Gain Satellite and communication devices/phones, Military strength power banks and Mobile devices such as iDevices and Android Devices.<br>
WET has been in testing for the last three years and is deemed safe to Humans, Plants and/or Foods items in the transmission or receiving area. Post extensive trials, this technology is now being released to the Military and defence market in India.</p>
<p>
<h3>The way Forward</h3>
Armed Forces across the world are in the process of transformation into an agile, lethal, versatile and networked force to meet the current and emerging threats and challenges across the entire spectrum of conflict including fourth generation asymmetric and virtual threats.<br>
With the dominance of warfare in the cyber space and the entry of the world in the information age envisages the edge of the battlefield being empowered to make decisions based on command intent and self-synchronisation derived from high quality shared awareness and collaboration.<br>
Wireless technologies will thus play a crucial role in coordinated deployments and improved situational awareness of Armed Forces in the future.
</p>

